# arithmetic operators
num1 = 56
num2 = 2
print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 % num2) # mod returns the remainder

# comparison operators
print(num1 > num2)
print(num1 < num2)
print(num1 >= num2)
print(num1 <= num2)
print(num1 == num2) #equal to
print(num1 != num2) #not equal to

#assignment operators
x = 14
print(x)
x += 2
print(x)
# logic operator - and or not
number = 5
print(number < 10 and number > 20) # all should be true ar else false
print(number < 10 or number > 20)
print(not(number < 10 or number > 20))#  reseverses the prev result


# operator precedence
result = (5 + 10 * 2 / 1  - 4)
print(result)




